#String and its function...............

name="rohit"

print("His name is ",name)

#5 functions in string...........

#len() -returns the length of the string...............

print("Length of the string is ",len(name))

#capitalize() - it will be capitalize the first character in the string.............

print("Capitalize the first character in the string",name.capitalize())

#lower() - change all char into lowercase

print("Converting all the characters into lower case :",name.lower())

#upper() - Converting all char into uppercase

print("Converting all char into uppercase :",name.upper())

#swapcase() -convert lowercase into uppercase and uppercase into lowercase...........

a="ScHooL"

print("Converting all  lowercase into uppercase and uppercase into lowercase :",a.swapcase())
